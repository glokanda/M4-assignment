// Use a conditional statement to check the current number is odd or even  and
// display a message in the console window.

for (let i = 1; i <= 15; i++){

    if( i % 2 === 0){

        console.log(`${i} is even `);
    }

    else{

        console.log(`${i} is odd `);
    }

}